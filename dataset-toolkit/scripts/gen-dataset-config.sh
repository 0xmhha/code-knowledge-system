#!/usr/bin/env bash
# gen-dataset-config.sh — generate the cks runtime config (yaml) + env file for a
# self-contained dataset directory, with all absolute paths resolved at run time.
# Re-runnable: moving the repos just means re-running this.
#
# Generalized from knowledge-data/pr-{14,58}/gen-pr*-config.sh. One dataset dir
# holds its own ckg/ + ckv/ + _src/ + logs/ + cks-<name>.{yaml,env}; the config
# is INDEPENDENT (never references another dataset's DB).
#
# Emits into the dataset dir:
#   cks-<NAME>.yaml   absolute-path cks config (ckg + ckv [+ optional domain])
#   cks-<NAME>.env    shell exports the coding-agent plugin's .mcp.json needs
#
# Usage:
#   DATASET=/abs/knowledge-data/pr-14 NAME=pr14 ./gen-dataset-config.sh
#
# Optional overrides:
#   SRC_ROOT       source_root for ckg (default: $DATASET/_src)
#   CKS_ROOT       code-knowledge-system checkout (default: 2 levels up via repo layout)
#   CKG_REPO       code-knowledge-graph checkout
#   CKV_REPO       code-knowledge-vector checkout
#   EMBED_MODEL    embedding model name (default: bge-m3)
#   CKV_OLLAMA_ENDPOINT  ollama url (default: http://localhost:11434)
#   DOMAIN_PROJECT_DIR / DOMAIN_CORPUS_DIR / GLOSSARY_PATH
#                  set all three to wire a cks domain project (optional; omit for
#                  datasets whose domain knowledge lives in ckv directly)
set -euo pipefail

abs() { ( cd "$1" 2>/dev/null && pwd ) || { echo "ERROR: path not found: $1" >&2; exit 1; }; }

DATASET="$(abs "${DATASET:?set DATASET=/abs/path/to/dataset/dir}")"
NAME="${NAME:?set NAME=short dataset name (used in filenames, e.g. pr14)}"
SRC_ROOT="$(abs "${SRC_ROOT:-$DATASET/_src}")"

# Default repo layout: code-knowledge-{system,graph,vector} as siblings.
CKS_ROOT="$(abs "${CKS_ROOT:-$DATASET/../../code-knowledge-system}")"
CKG_REPO="$(abs "${CKG_REPO:-$CKS_ROOT/../code-knowledge-graph}")"
CKV_REPO="$(abs "${CKV_REPO:-$CKS_ROOT/../code-knowledge-vector}")"
OLLAMA_URL="${CKV_OLLAMA_ENDPOINT:-http://localhost:11434}"
EMBED_MODEL="${EMBED_MODEL:-bge-m3}"

CKS_MCP_BIN="$CKS_ROOT/bin/cks-mcp"
CONFIG="$DATASET/cks-$NAME.yaml"
ENVFILE="$DATASET/cks-$NAME.env"
mkdir -p "$DATASET/logs/footprint" "$DATASET/logs/audit"

cat > "$CONFIG" <<YAML
# GENERATED by gen-dataset-config.sh — do not hand-edit; re-run to refresh.
# Dataset: $NAME  (independent — do NOT link to another dataset's DB).
version: 1

backends:
  # ckg.path is a FILE (single sqlite graph.db); ckv.path is a DIRECTORY —
  # ckv.Open requires the dir holding vector.db + manifest.json (the manifest
  # carries the embedding identity checked at open). Not an inconsistency.
  ckg:
    path: "$DATASET/graph-db/graph.db"
    source_root: "$SRC_ROOT"
    binary_path: "$CKG_REPO/bin/ckg"
    timeout_ms: 5000
  ckv:
    path: "$DATASET/vector-db"
    timeout_ms: 3000
    embed_model: "$EMBED_MODEL"
    ollama_url: "$OLLAMA_URL"
    binary_path: "$CKV_REPO/bin/ckv"

listen:
  http_addr: "127.0.0.1:8080"
  mcp_stdio: true

logging:
  level: "info"
  mode: "prod"
  footprint_dir: "$DATASET/logs/footprint"
  audit_dir: "$DATASET/logs/audit"

sanitize:
  rules_path: "$CKS_ROOT/policies/sanitization_rules.yaml"
  default_action: "drop"
  fail_closed_on_unknown_rule: true
YAML

# Optional cks domain project wiring (all three must be set together).
if [ -n "${DOMAIN_PROJECT_DIR:-}" ] && [ -n "${DOMAIN_CORPUS_DIR:-}" ]; then
  cat >> "$CONFIG" <<YAML

domain:
  project_dir: "$(abs "$DOMAIN_PROJECT_DIR")"
  corpus_dir: "$(abs "$DOMAIN_CORPUS_DIR")"
YAML
  if [ -n "${GLOSSARY_PATH:-}" ]; then
    cat >> "$CONFIG" <<YAML

vocab:
  glossary_path: "$GLOSSARY_PATH"
YAML
  fi
fi

cat > "$ENVFILE" <<ENV
# GENERATED by gen-dataset-config.sh — source this to point coding-agent's MCP at $NAME.
export CKS_MCP_BIN="$CKS_MCP_BIN"
export CKS_CONFIG="$CONFIG"
export CKV_OLLAMA_ENDPOINT="$OLLAMA_URL"
ENV

echo "generated:"
echo "  $CONFIG"
echo "  $ENVFILE"
[ -x "$CKS_MCP_BIN" ]      || echo "  WARN: cks-mcp not built ($CKS_MCP_BIN)"
[ -x "$CKG_REPO/bin/ckg" ] || echo "  WARN: ckg not built ($CKG_REPO/bin/ckg)"
[ -x "$CKV_REPO/bin/ckv" ] || echo "  WARN: ckv not built ($CKV_REPO/bin/ckv)"
echo ""
echo "serve:  source \"$ENVFILE\"  &&  \"$CKS_MCP_BIN\" -config \"$CONFIG\""
